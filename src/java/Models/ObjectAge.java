package Models;

/**
 *
 * @author huyca
 */
public class ObjectAge {
    private int ageId;
    private String age;

    public ObjectAge() {
    }

    public ObjectAge(int ageId, String age) {
        this.ageId = ageId;
        this.age = age;
    }

    public int getAgeId() {
        return ageId;
    }

    public void setAgeId(int ageId) {
        this.ageId = ageId;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
    
    
}
