package Models;

/**
 *
 * @author USER
 */
public class ImageBackground {
    private String name;
    private String url;
    private int status;
    private int id;

    public ImageBackground() {
    }

    public ImageBackground(String name, String url, int status, int id) {
        this.name = name;
        this.url = url;
        this.status = status;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

   
    
    
}
